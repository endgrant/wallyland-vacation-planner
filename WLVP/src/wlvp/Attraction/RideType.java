package wlvp.Attraction;

/**
 * Enumerator representing different types of rides available in the theme park.
 */
public enum RideType {
    /** High-speed, adrenaline-filled ride with loops and drops */
    Rollercoaster,
    
    /** Rides specifically designed for young children */
    KidRide,
    
    /** Water-based attractions such as slides and wave pools */
    WaterParkRide,
    
    /** Games where guests can win prizes */
    PrizeGames,
    
    /** Thrill rides designed for maximum excitement and intensity */
    ThrillRide;
}
